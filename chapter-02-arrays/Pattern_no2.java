public class Pattern_no2 {

  public static void main(String[] args) {
    System.out.println("\nHey in this program i will make a [*] pattern");
    int i, j;
    for (i = 1; i <= 5; i++) {
      for (j = 1; j <= i; j++) {
        System.out.print(i);
      }
      System.out.println(0);
    }
  }
}
