public class datatype_rem {

  public static void main(String[] args) {
    System.out.println(
      "In this program you will see some Datatype resulting convention!"
    );
    float f = 2.4f + 6; // Float + int = float
    int a = 1 + 4; // Int + int = int
    // this 1.3 is double is by default
    byte y = 4;
    short z = 3;
    int x = y + z; // byte + short = int
    System.out.println(x);
    System.out.println(a);
    System.out.println(f);
  }
}
