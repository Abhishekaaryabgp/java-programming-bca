class identifier {

  public static void main(String[] args) {
    byte age = 21; // byte, short , int is integer types
    int q = 31;
    long dino = 5565L;
    char ch = 'A';
    Float p = 2.0f;
    double d = 33223.4;
    String str = "Abhishek Aarya";
    boolean boo = true;
    System.out.println(dino);
    System.out.print(age); // ln is used to print new line
    System.out.println(ch);
    System.out.println(p);
    System.out.println(d);
    System.out.println(age);
    System.out.println(q);
    System.out.println(str);
    System.out.println(boo);
  }
}
